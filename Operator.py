# OPERATOR ARITMATIKA
print(1 + 1)   # Pertambahan
print(2 - 2)   # Pengurangan
print(3 * 3)   # Perkalian
print(7 / 3)   # Pembagian float
print(6 // 3)  # Pembagian int
print(5 % 2)   # Sisa bagi Modulus
print(2 ** 3)  # Perpangkatan

# OPERATOR BOOLEAN
print(10 == 10)   # Sama dengan
print(15 != 12)   # Tidak sama dengan
print(8 > 2)      # Lebih besar
print(10 < 20)    # Lebih kecil
print(17 >= 17)   # Lebih besar sama dengan
print(14 <= 14)   #Lebih kecil sama dengan

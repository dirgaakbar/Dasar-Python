# FOR ADALAH LOOP YANG SUDAH KITA KETAHUI HASILNYA
for i in range(5):	# Angkanya akan mulai dari o hingga 4
	print(i)

# WHILE ADALAH LOOP DENGAN SYARAT
angka = 1		# Angkanya mulai dari 1
while angka <= 5:	# Jika angkanya sama atau kurang dari 5 artinya True
	print(angka)
	angka += 1	# Programnya tambah 1 hingga mencapai 5

